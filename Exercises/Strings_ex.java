
public class Strings_ex {
    public static void main(String[] args) {
        int a = 34;
        String x = "123";
        System.out.println(a+2);
        System.out.println(Integer.parseInt(x) + 2);
        System.out.println(Integer.toString(a) + 2);
        System.out.println(String.valueOf(a) + 2);
    }
}


